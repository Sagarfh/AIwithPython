#Bitwise Operators
a = int(input("Enter value of a : "))
b = int(input("Enter value of b : "))

#bitwise AND
print("bitwise AND : ",a & b)

#bitwise OR
print("bitwise OR : ",a | b)

#bitwise NOT
print("bitwise NOT : ",~a)

#bitwise XOR
print("bitwise XOR : ",a^b)

#bitwise right shift
print("bitwise right shift : ",a >> 2)

#bitwise left shift
print("bitwise left shift : ",a << 2)