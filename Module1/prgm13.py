#Assignment Operator

a=10
b=a
print(b)

#Add and assign value
b+=a
print("Addition = ",b)

#Subtract and assign value
b-=a
print("Subtract = ",b)

#multiplaction and assign value
b*=a
print("multiplaction = ",b)

#bitwise left shift
a=1
b=2
b<<=a
print("left shift = ",b)

#bitwise right shift
b>>=a
print("right shift = ",b)